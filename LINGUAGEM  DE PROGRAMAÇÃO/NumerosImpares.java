package Nivelamento2;

public class NumerosImpares {

    public static void main(String[] args) {

        //   Integer impar = ;
        for (int i = 0; i <= 90; i++) {

            if (i % 2 == 0) {
                System.out.println();

            } else {
                System.out.println(i);
            }
        }

    }

}
